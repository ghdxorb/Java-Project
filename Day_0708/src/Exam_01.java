
public class Exam_01 {
	public static void main(String[] args) {
		//표준(콘솔) 입출력 System
		System.out.print("Hello\t World!");//줄바꿈 없음
		System.out.println();
		System.out.print("Hello \nWorld!");//줄바꿈 없음
		System.out.print("\n");
		System.out.println("Hello World!");//자동 줄바꿈
		System.out.println("Hello World!");//자동 줄바꿈
		System.out.println("Hello World!\n");//자동 줄바꿈
		
		// \n -> 줄바꿈, \t -> tab(8칸 우측 띄우기)
		
		
		
		
	}

}
